void Primitives_RenderCube() {
    VertexArray_Bind(cubeVAO);
    RenderCommand_Draw(36);
    glBindVertexArray(0);
}

void Primitives_RenderQuad() {
    VertexArray_Bind(quadVAO);
    glDrawArrays(GL_TRIANGLE_STRIP, 0, 4);
    glBindVertexArray(0);
}